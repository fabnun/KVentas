    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nuevo = JOptionPane.showInputDialog(this, "ingrese el usuario nuevo?", "");
        if (nuevo.length() > 64) {
            nuevo = nuevo.substring(0, 64);
        }
        nuevo = nuevo.trim().toUpperCase();
        if (nuevo != null) {
            try {
                Constantes.db.nuevoUsuario(nuevo, "", "", "", "", "");
                dcm.addElement(nuevo);
                jComboBox1.setSelectedItem(nuevo);
                jTextField4.requestFocus();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Ya existe el usuario " + nuevo);
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String usuario = (String) jComboBox1.getSelectedItem();
        if (JOptionPane.showConfirmDialog(this, "Desea eliminar al usuario " + usuario + "?", "", JOptionPane.YES_NO_OPTION) == 0) {
            try {
                List<Object[]> lista = Constantes.db.getNotas(usuario);
                for (Object[] o : lista) {
                    Constantes.db.eliminarNota(o[0].toString(), o[1].toString());
                }
                lista = Constantes.db.getPermisos(usuario);
                for (Object[] o : lista) {
                    if ((Long) o[2] == 1l) {
                        Constantes.db.setPermiso(usuario, (Integer) o[0], false);
                    }
                }
                Constantes.db.eliminarUsuario(usuario);
                dcm.removeElementAt(jComboBox1.getSelectedIndex());
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

