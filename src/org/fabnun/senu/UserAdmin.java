    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String usuario = (String) jComboBox1.getSelectedItem();
        String claveAnt = new String(jPasswordField1.getPassword());
        String claveNu1 = new String(jPasswordField2.getPassword());
        String claveNu2 = new String(jPasswordField3.getPassword());
        String msg = null;
        if (claveNu1.equals(claveNu2)) {
            try {
                if (Constantes.db.autorizar(usuario, claveAnt)) {
                    Constantes.db.setClave(usuario, claveNu1);
                } else {
                    msg = "No es correcta la clave antigua";
                }
            } catch (Exception e) {
                msg = "Error desconocido al modificar clave";
            }
        } else {
            msg = "No coincide la clave nueva con su repetici√≥n";
        }
        if (msg != null) {
            JOptionPane.showMessageDialog(this, msg);
        } else {
            jDialog1.setVisible(false);
            JOptionPane.showMessageDialog(this, "Clave de " + usuario + " modificada");
        }
    }//GEN-LAST:event_jButton5ActionPerformed
