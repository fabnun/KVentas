    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        final String usuario = (String) jComboBox1.getSelectedItem();
        for (JCheckBox box : boxes) {
            box.getParent().remove(box);
        }
        boxes = new LinkedList<JCheckBox>();
        try {
            List<Object[]> permisos = Constantes.db.getPermisos(usuario);
            JCheckBox check;
            for (Object[] obj : permisos) {
                check = new JCheckBox(obj[1].toString());
                check.setSelected((Long) obj[2] == 1);
                jDialog2.add(check);
                boxes.add(check);
                final int idperm = (Integer) obj[0];
                final JCheckBox selected = check;
                check.addActionListener(new ActionListener() {

                    public void actionPerformed(ActionEvent e) {
                        try {
                            Constantes.db.setPermiso(usuario, idperm, selected.isSelected());
                        } catch (Exception ex) {
                            ex.printStackTrace();
                        }
                    }
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        jDialog2.pack();
        jDialog2.setLocationRelativeTo(this);
        jDialog2.setVisible(true);
    }//GEN-LAST:event_jButton3ActionPerformed
