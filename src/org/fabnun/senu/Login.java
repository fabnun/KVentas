    private void checkLogin() throws SQLException {
        String usuario = jTextField1.getText().trim();
        String clave = new String(jPasswordField1.getPassword()).trim();
        escritorio.usuario = Constantes.db.autorizar(usuario, clave) ? usuario : null;
        if (escritorio.usuario == null) {
            jLabel3.setText("Usuario y/o clave erroneos...");
        } else {
            setVisible(false);
        }
    }
